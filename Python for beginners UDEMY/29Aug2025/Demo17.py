r1 = range(5)  # to define a range
print(type(r1))
for i in r1:
    print(i)

print("\n r2 starts from here")
r2 = range(1, 6)  # to define a starting and ending part in range
print(type(r2))
for i in r2:
    print(i)

print("\n r3 starts from here")  # to add the steps in range
r3 = range(1, 15, 3)
print(type(r3))
for i in r3:
    print(i)
