lst1 = [x for x in input("Enter three numbers separated by commas : ").split(",")]
print(" this is the list" ,lst1)
lst2 = [int(x) for x in input("Enter three integers numbers separated by commas : ").split(",")]
print(" this is the list of integers" ,lst2)
lst3 = [float(x) for x in input("Enter three floating separated by commas : ").split(",")]
print(" this is the list of floating numbers" ,lst3)